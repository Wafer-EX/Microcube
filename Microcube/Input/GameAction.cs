namespace Microcube.Input
{
    /// <summary>
    /// Represents a game actions.
    /// </summary>
    public enum GameAction
    {
        /// <summary>
        /// Abstract up action.
        /// </summary>
        Up,

        /// <summary>
        /// Abstract down action.
        /// </summary>
        Down,
        
        /// <summary>
        /// Abstract left action.
        /// </summary>
        Left,
        
        /// <summary>
        /// Abstract right action.
        /// </summary>
        Right,
        
        /// <summary>
        /// Abstract enter (ok) action.
        /// </summary>
        Enter,
        
        /// <summary>
        /// Abstract escape (back) action.
        /// </summary>
        Escape
    }
}